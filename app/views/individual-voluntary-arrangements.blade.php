			<h3>Benefits of an IVA</h3>
			<ul class="custom">
				<li>All pressure from your creditors is removed</li>
				<li>You will only repay what you can reasonably afford </li>
				<li>All correspondence and queries from your creditors are dealt with on your behalf </li>
				<li>You will make only one monthly repayment </li>
				<li>At the end of the IVA, which is usually a 5 year period, you will be debt free from the debts included within your IVA and 
ready to start afresh financially </li>
